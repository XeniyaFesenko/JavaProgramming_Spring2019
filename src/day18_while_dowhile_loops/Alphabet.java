package day18_while_dowhile_loops;

public class Alphabet {


	public static void main(String[] args) {
		char letter = 'a';
		
		while(letter <= 'z') {
			System.out.print(letter+" ");
			letter++;
		}
		
		System.out.println();//new line
		
		char capitals = 'A';
		
		while(capitals <= 'Z') {
			System.out.print(capitals+" ");
			capitals++;
		}
		
		//print lowercase in reverse
		
		
	}
}
